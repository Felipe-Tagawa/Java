package operadores;

public class Operadores {

	public static void main(String[] args) {
	byte num1 = 10;
	byte num2 = 13;
	
	System.out.println(num1 + num2);
	System.out.println(num1 - num2);
	System.out.println(num1 * num2);
	System.out.println(num1 / num2);
	System.out.println(num1 % num2);
	System.out.println(num1 == num2);
	System.out.println(num1 >= num2);
	System.out.println(num1 <= num2);
	System.out.println(num1 != num2);
	}

}
